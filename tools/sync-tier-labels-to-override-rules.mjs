#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const startMarker = "-- BEGIN MANAGED ITEM TIER LABELS";
const endMarker = "-- END MANAGED ITEM TIER LABELS";

function parseArgs(argv) {
  const args = {};
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (!arg.startsWith("--")) {
      continue;
    }
    const key = arg.slice(2);
    const value = argv[index + 1] && !argv[index + 1].startsWith("--") ? argv[++index] : true;
    args[key] = value;
  }
  return args;
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8").replace(/^\uFEFF/, ""));
}

function detectNewline(text) {
  return text.includes("\r\n") ? "\r\n" : "\n";
}

function renderBlock(tierConfig, newline) {
  const labels = tierConfig.labels;
  const entries = tierConfig.tiers.map((entry) => ({
    code: entry.code || entry.Key,
    tier: entry.tier,
    label: labels[entry.tier],
  }));
  const seen = new Set();
  const lines = [
    `        ${startMarker}`,
    "        -- Generated by tools/sync-tier-labels-to-override-rules.mjs.",
    "        -- Keep tier tags out of item-names.json; append them here so generated rare/magic names stay intact.",
  ];

  for (const entry of entries) {
    if (!entry.code || !entry.label) {
      throw new Error(`Invalid tier entry: ${JSON.stringify(entry)}`);
    }
    if (seen.has(entry.code)) {
      throw new Error(`Duplicate item code in tier labels: ${entry.code}`);
    }
    seen.add(entry.code);

    lines.push(
      "        {",
      `            -- item tier: ${entry.tier}`,
      `            code = "${entry.code}",`,
      '            location = { "onground", "onplayer", "equipped", "atvendor" },',
      `            suffix = " {gray}${entry.label}"`,
      "        },",
    );
  }

  lines.push(`        ${endMarker}`);
  return `${lines.join(newline)}${newline}`;
}

function replaceManagedBlock(text, block, newline) {
  const start = text.indexOf(startMarker);
  const end = text.indexOf(endMarker);

  if (start !== -1 || end !== -1) {
    if (start === -1 || end === -1 || end < start) {
      throw new Error("Malformed managed item tier label block markers");
    }

    const lineStart = text.lastIndexOf(newline, start);
    const replaceStart = lineStart === -1 ? 0 : lineStart + newline.length;
    const replaceEnd = text.indexOf(newline, end);
    const afterEnd = replaceEnd === -1 ? text.length : replaceEnd + newline.length;
    return `${text.slice(0, replaceStart)}${block}${text.slice(afterEnd)}`;
  }

  const closePattern = new RegExp(`${newline.replace(/\r/g, "\\r").replace(/\n/g, "\\n")}    \\}${newline.replace(/\r/g, "\\r").replace(/\n/g, "\\n")}\\}\\s*$`);
  const closeMatch = text.match(closePattern);
  if (!closeMatch || closeMatch.index === undefined) {
    throw new Error("Could not find override_rules.lua closing rules table");
  }

  const insertAt = closeMatch.index + newline.length;
  return `${text.slice(0, insertAt)}${block}${text.slice(insertAt)}`;
}

const args = parseArgs(process.argv.slice(2));
const write = args.write === true || args.write === "true";
const check = args.check === true || args.check === "true";

if (!write && !check) {
  console.error("Pass --write to update override_rules.lua or --check to verify it.");
  process.exit(2);
}

const tierConfigPath = args.mapping ?? "localization/item-tier-labels.json";
const overrideRulesPath = args.out ?? "mpq-data/D2RLAN/Filters/override_rules.lua";

const tierConfig = readJson(tierConfigPath);
const currentText = fs.readFileSync(overrideRulesPath, "utf8");
const newline = detectNewline(currentText);
const block = renderBlock(tierConfig, newline);
const nextText = replaceManagedBlock(currentText, block, newline);
const changed = nextText !== currentText;

if (check && changed) {
  console.error(`${overrideRulesPath} item tier label block is out of date.`);
  process.exit(1);
}

if (write && changed) {
  fs.mkdirSync(path.dirname(overrideRulesPath), { recursive: true });
  fs.writeFileSync(overrideRulesPath, nextText, "utf8");
}

console.log(JSON.stringify({
  overrideRulesPath,
  mapping: tierConfigPath,
  write,
  check,
  changed,
  rules: tierConfig.tiers.length,
}, null, 2));
